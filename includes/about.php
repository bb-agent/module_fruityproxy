<b>FruityProxy</b> by @xtr4nge
<br>
<br>- FruityProxy can import mitmproxy inline scripts.
<br>- It is possible to set an upstream proxy.
<br>- If the upstream proxy is set, the corresponding module will start automatically.
<br>
<br>One of the following BlackBulb modules is required for upstream option:
<br> - SSLstrip2 {or} SSLstrip {or} MITMf
<br>
<br>BlackBulb: http://www.blackbulb.com
<br>libmproxy:  https://mitmproxy.org/doc/scripting/libmproxy.html